import Foundation

/// Enum representing the type of macro being used
enum MacroType: String {
    /// The macro type for `@ObservableDefaults`
    case observableDefaults = "@ObservableDefaults"
    /// The macro type for `@ObservableCloud`
    case observableCloud = "@ObservableCloud"
}
